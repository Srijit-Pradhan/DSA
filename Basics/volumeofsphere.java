package Basics;

public class volumeofsphere {
    public static void main(String[] args) {
        // volume of sphere = (4/3)*pi*r*r*r
        double r = 9;
        double pi = 3.141592;
        double volume = (4.0 / 3.0) * pi * r * r * r;
        System.out.println("Volume of sphere is: " + volume);
    }
}
