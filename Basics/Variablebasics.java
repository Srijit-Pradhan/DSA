package Basics;

public class Variablebasics {
    public static void main(String[] args) {
        int x = 1;
        System.out.println(x);
        int y;// declaration
        y = 3;// initialization
        System.out.println("this is y = " + y);
        x = x + 2;
        System.out.println("this is x = " + x);
        x = x - 2;
        System.out.println("this is updated x = " + x);
        x=x*2;
        System.out.println("this is multiplied x = " + x);
        x=x/2;
        System.out.println("this is divided x = " + x);
    }
}
